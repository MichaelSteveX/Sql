# INNER JOIN 

SELECT * FROM STUDENT
INNER JOIN COURSE
ON STUDENT.ID = COURSE.ID;

SELECT * FROM STUDENT AS S
INNER JOIN COURSE AS C
ON S.ID = C.ID;

# LEFT JOIN 

SELECT * FROM STUDENT
LEFT JOIN COURSE
ON STUDENT.ID = COURSE.ID;

# RIGHT JOIN

SELECT * FROM STUDENT
RIGHT JOIN COURSE
ON STUDENT.ID = COURSE.ID;

# FULL JOIN 

SELECT * FROM STUDENT
LEFT JOIN COURSE
ON STUDENT.ID = COURSE.ID
UNION
SELECT * FROM STUDENT
RIGHT JOIN COURSE
ON STUDENT.ID = COURSE.ID;


# LEFT EXCLUSIVE 
SELECT * 
FROM STUDENT
LEFT JOIN COURSE
ON STUDENT.ID = COURSE.ID
WHERE COURSE.ID IS NULL;

# RIGHT EXCLUSIVE 
SELECT * 
FROM STUDENT
RIGHT JOIN COURSE
ON STUDENT.ID = COURSE.ID
WHERE STUDENT.ID IS NULL;




